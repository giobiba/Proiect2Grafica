class Cube {
public:
	unsigned int VAO, VBO;
	glm::vec3 position, size, color;
	Shader shader;

	Cube(Shader shader, glm::vec3 position = glm::vec3(1.0f), glm::vec3 size = glm::vec3(1.0f), glm::vec3 color = glm::vec3(1.0f)) : shader(shader), position(position), size(size), color(color) { createBuffers(); };

	void createBuffers() {
		glGenVertexArrays(1, &VAO);
		glGenBuffers(1, &VBO);

		glBindBuffer(GL_ARRAY_BUFFER, VBO);
		glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

		glBindVertexArray(VAO);

		glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
		glEnableVertexAttribArray(0);

		glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
		glEnableVertexAttribArray(1);

		glVertexAttribPointer(2, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));
		glEnableVertexAttribArray(2);

		glEnable(GL_DEPTH_TEST);
	}

	void draw(glm::mat4 projection, glm::mat4 view, GLfloat angle, bool isPlanet, float velocity, int direction) {
		shader.use();
		glm::mat4 model = glm::mat4(1.0f);
		
		model = glm::translate(model, position);
		model = glm::scale(model, size);
		model = glm::rotate(model, angle, glm::vec3(0.0f,1.0f, 0.5f));

		if (isPlanet)
		{
			float radius = glm::distance(position,glm::vec3(0.0f,1.0f,0.0f));
			position = glm::vec3(sin(glfwGetTime() * direction * velocity) * radius , position[1], cos(glfwGetTime() * direction * velocity) * radius);
		}
		shader.SetMatrix4("model", model);
		shader.SetMatrix4("projection", projection);
		shader.SetMatrix4("view", view);
		shader.SetVector3f("objectColor", color);

		glBindVertexArray(VAO);
		glDrawArrays(GL_TRIANGLES, 0, 36);
	}

};